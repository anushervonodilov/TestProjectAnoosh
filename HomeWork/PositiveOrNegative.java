package HomeWork;

public class PositiveOrNegative {
    public static void main(String[] args) {

        int num = 0;

        if(num > 0){
            System.out.println(num + " is positive");
        }else if(num == 0){
            System.out.println(num + " is 0");
        }else{
            System.out.println(num + " is negative");
        }



    }
}
